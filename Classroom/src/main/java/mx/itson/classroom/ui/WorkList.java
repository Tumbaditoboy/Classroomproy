        private void loadWork(){
        List<Work> work = WorkDAO.getAll();
        DefaultTableModel modelo =(DefaultTableModel) tblWork.getModel();
        modelo.setRowCount(0);
        
        for (Work w : work){
            modelo.addRow(new Object[] {
                w.getId(),
                w.getDate(),
                w.getFile_Name(),
                w.getId_Assignment(),
                w.getId_Student()
            });
        }
    }
